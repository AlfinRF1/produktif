   private void loadTransaksiBeli() {
        DefaultTableModel model = (DefaultTableModel) tabell_trbeli.getModel();
        model.setRowCount(0); // Clear existing data
        
        try {
            Connection conn = koneksi.koneksiDB();
            // Change the SQL query to target the correct table
            String sql = "SELECT * FROM transaksi_beli";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            
            while (rs.next()) {
                Object[] row = {
                    rs.getString("kode_pembelian"),
                    rs.getDate("tanggal_pembelian"),
                    rs.getInt("Jumlah_pembelian"),
                    rs.getDouble("Harga_Barang"),
                    rs.getDouble("total_harga"),
                    rs.getString("No_Ktp_supplier"),
                    rs.getString("Id_Barang")
                };
                model.addRow(row);
            }
            
            rs.close();
            stmt.close();
            conn.close();
        } catch (SQLException e) {
            System.err.println("Got an exception! ");
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(this, 
                "Error loading data: " + e.getMessage(), 
                "Database Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }
        
