    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
       int selectedRow = tabell_trbeli.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, 
                "Pilih transaksi yang akan dihapus terlebih dahulu!", 
                "Perhatian", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Anda yakin ingin menghapus transaksi ini?", 
            "Konfirmasi Hapus", 
            JOptionPane.YES_NO_OPTION);
            
        if (confirm == JOptionPane.YES_OPTION) {
            String kodePembelian = tabell_trbeli.getValueAt(selectedRow, 0).toString();
            
            try {
                Connection conn = koneksi.koneksiDB();
                String sql = "DELETE FROM transaksi_beli WHERE kode_pembelian = ?";
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setString(1, kodePembelian);
                
                int rowsAffected = stmt.executeUpdate();
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, 
                        "Transaksi berhasil dihapus!", 
                        "Sukses", 
                        JOptionPane.INFORMATION_MESSAGE);
                    loadTransaksiBeli(); // Refresh the table
                } else {
                    JOptionPane.showMessageDialog(this, 
                        "Gagal menghapus transaksi!", 
                        "Error", 
                        JOptionPane.ERROR_MESSAGE);
                }
                
                stmt.close();
                conn.close();
            } catch (SQLException e) {
                System.err.println("Got an exception! ");
                System.err.println(e.getMessage());
                JOptionPane.showMessageDialog(this, 
                    "Error menghapus data: " + e.getMessage(), 
                    "Database Error", 
                    JOptionPane.ERROR_MESSAGE);
            }
        }
    
    }//GEN-LAST:event_btn_hapusActionPerformed

    private void tabell_trbeliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabell_trbeliMouseClicked
                                               
    int selectedRow = tabell_trbeli.getSelectedRow();
    if (selectedRow != -1) {
        
        String kodePembelian = tabell_trbeli.getValueAt(selectedRow, 0).toString();
        
        try {
            Connection conn = koneksi.koneksiDB();
            String sql = "SELECT * FROM transaksi_beli WHERE kode_pembelian = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, kodePembelian);
            ResultSet rs = stmt.executeQuery();
            
            if (rs.next()) {
              
                StringBuilder details = new StringBuilder();
                details.append("Detail Transaksi Pembelian\n\n");
                details.append("Kode Pembelian: ").append(rs.getString("kode_pembelian")).append("\n");
                details.append("Tanggal Pembelian: ").append(rs.getDate("tanggal_pembelian")).append("\n");
                details.append("Jumlah Pembelian: ").append(rs.getInt("Jumlah_pembelian")).append("\n");
                details.append("Harga Barang: Rp ").append(String.format("%,.2f", rs.getDouble("Harga_Barang"))).append("\n");
                details.append("Total Harga: Rp ").append(String.format("%,.2f", rs.getDouble("total_harga"))).append("\n");
                details.append("No KTP Supplier: ").append(rs.getString("No_Ktp_supplier")).append("\n");
                details.append("ID Barang: ").append(rs.getString("Id_Barang")).append("\n");
                
                JOptionPane.showMessageDialog(this, 
                    details.toString(), 
                    "Detail Transaksi", 
                    JOptionPane.INFORMATION_MESSAGE);
            }
            
            rs.close();
            stmt.close();
            conn.close();
        } catch (SQLException e) {
            System.err.println("Got an exception! ");
            System.err.println(e.getMessage());
            JOptionPane.showMessageDialog(this, 
                "Error mengambil data: " + e.getMessage(), 
                "Database Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_tabell_trbeliMouseClicked

